<br><p style="font-size: 30px;"><u><b>Alimentos nutricionalmente densos</b></u></p><br>
<ul class="text-left">
    <li>Frutos Secos</li>
    <li>Aguacate</li>
    <li>Coco</li>
    <li>Higos</li>
    <li>Uvas pasas y fruta desecada</li>
    <li>Pescados azules</li>
    <li>Huevos</li>
    <li>Aceitunas</li>
    <li>Aceite de oliva virgen</li>
</ul>
<p class="text-justify mt-5"><b>Combinando estos alimentos en nuestros platos</b> con otros más, podemos conseguir platos altamente calóricos, pero muy ricos en nutrientes. Lo que queremos conseguir es un aporte extra de calorías mediante alimentos ricos y saciantes, no mediante atracones de comida basura que harán que tengamos hambre a las dos horas de haber comido.</p>
<p class="text-justify mt-5">Aquí, lo que también nos interesa es <b>conseguir más calorías a través de la comida de las que gastamos</b> en nuestro día a día, así que debemos hacerlo de forma saludable, con la mayor calidad posible, y para ello, esta clase de alimentos son clave para introducirlos.</p>

<br><p style="font-size: 30px;"><u><b>Menú ejemplo para aumentar de peso</b></u></p><br>
<p class="text-justify mt-5">Para conseguir aumentar de peso, no nos deberíamos fijar en las comidas de un sólo día; es aconsejado evaluar nuestra alimentación en su conjunto, de forma semanal o incluso mensual. Para hacerlo posible, el profesional más cualificado para ello es el dietista-nutricionista, el cual debe evaluar la situación de cada persona de la manera más precisa posible.</p>
