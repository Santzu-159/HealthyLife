<p class="text-justify mb-5">Aparte de la alimentación, es necesario que los padres de niños muy delgados, busquen la causa por la que los niños no ganan peso. Para ello, es conveniente que consulten a un médico para un chequeo de la salud. Problemas como la pequeña <b>producción de leche materna</b>, en el caso de los bebés, pueden ser la causa. Otros problemas pueden estar relacionados con el intestino. Habría que consultar con el <b>pediatra de los niños</b>.</p>
