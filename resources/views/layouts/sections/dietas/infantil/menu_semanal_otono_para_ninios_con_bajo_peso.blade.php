
<p class="text-justify">Si el niño no tiene hambre y tiene bajo peso no te desesperes. Los niños <b>menores de 4 años</b> por lo general son desordenados para comer y comen en ocasiones poco. No lo fuerces a comer, pero tampoco le des lo que él pide ni le permitas que sólo coma lo que le gusta. <b>La naturaleza es sabia y si necesita comer</b>, el niño sentirá hambre y comerá. Si tu hijo tiene un peso significativamente bajo, apoya el consumo de grasa y calorías adicionales con bebidas saludables como la leche.</p>
