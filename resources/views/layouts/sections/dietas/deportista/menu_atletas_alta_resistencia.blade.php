<p class="text-justify mt-5">El menú para deportistas de resistencia debe repartir correctamente el consumo de alimentos durante el día, para así tener tiempo suficiente para realizar una correcta digestión previa al entrenamiento, especialmente durante los días previos a la competición.</p>
<p class="text-justify mt-5">Es fundamental beber la cantidad necesaria de agua a lo largo del día, evitar la ingesta de grasas, aceites y fritos y reducir la cantidad de alimentos con alto contenido de fibra. Conscientes de todo esto, aquí os dejamos un menú altamente recomendable para este tipo de deportistas de resistencia.</p>
<br><p style="font-size: 40px;"><b>Menú</b></p><br>
<p class="text-left mt-5">Desayuno</p>
<p class="text-justify mt-5">El desayuno es la comida más importante ya que te permite empezar el día correctamente o en cambio comenzar ya con el pie izquierdo. Lo primero que debes hacer al levantarte de la cama es tomarte dos vasos de agua en ayunas.</p>
<p class="text-justify mt-5">Respecto a la alimentación, puedes variar entre los diferentes alimentos que te dejamos a continuación:</p>
<ul class="text-left">
    <li>Una taza de cereales integrales con leche desnatada.</li>
    <li>Dos rebanadas de pan integral con huevo y 30 gramos de queso fresco o atún.</li>
    <li>Un tomate trinchado.</li>
    <li>Una pieza de fruta fresca o un vaso de zumo de naranja natural.</li>
    <li>Un vaso de té o un café descafeinado.</li>
</ul>
<p class="text-justify mt-5">En caso de mezclar con leche, importante que sea desnatada.</p>
<p class="text-left mt-5">Almuerzo</p>
<p class="text-justify mt-5">Es importante comer a mitad de mañana para acelerar nuestro metabolismo y que nunca deje de funcionar: - Dos rebanadas de pan integral. - Un plátano. - Un yogur desnatado. - Agua mineral.</p>
<p class="text-left mt-5">Comida</p>
<p class="text-justify mt-5">Previamente a la hora de la comida debemos intentar beber altas cantidades de agua, además de llevar una dieta que incluya los siguientes alimentos:</p>
<ul class="text-left">
    <li>Ensalada de vegetales verdes.</li>
    <li>Un plato de verduras (judías verdes, menestra, acelgas, etc.)</li>
    <li>UUna ración con una fuerte carga de hidratos de carbono (pasta, pan, arroz, patatas, etc.)</li>
    <li>Unos 90-120 gramos de carne roja o 180-240 gramos de pescado.</li>
    <li>Una pieza de fruta fresca.</li>
    <li>Un yogurt desnatado</li>
</ul>
<p class="text-left mt-5">Merienda</p>
<p class="text-justify mt-5">Los alimentos que se consumen a la hora de la merienda son similares a los que debemos tomar durante el desayuno: - Dos rebanadas de pan integral, pudiendo completarlas con mermelada o miel. - Un plátano. - Yogur desnatado. - Agua mineral.</p>
<p class="text-left mt-5">Cena</p>
<p class="text-justify mt-5">La cena debe ser ligera, pero también debe aportarnos la cantidad de energía necesaria que hemos perdido con nuestro entrenamiento. Es fundamental cenar como mínimo dos horas antes de acostarnos para que podamos realizar una correcta digestión, además de reducir en la medida de lo posible la ingesta de hidratos de carbono.</p>
<p class="text-justify mt-5">Cuantos más colores tenga la cena, mucho mejor.</p>
<ul class="text-left">
    <li>Plato de verduras o ensalada, que variará en función de lo que hayamos tomado durante la comida.</li>
    <li>Pechuga de pollo asada con un tomate natural trinchado.</li>
    <li>Sándwich de pan integral con pollo, jamón cocido bajo en grasa, atún, queso o dos huevos cocidos.</li>
    <li>Fruta fresca.</li>
    <li>Una taza de té o de café descafeinado si se desea a modo de postre.</li>
</ul>
