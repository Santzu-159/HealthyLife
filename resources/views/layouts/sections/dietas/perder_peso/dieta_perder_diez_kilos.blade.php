<p class="text-justify mt-5">La clave para adelgazar 10kg es tomar la decisión de querer hacerlo, es decir, está en tus manos conseguirlo, solo tienes que cambiar algunos hábitos y nosotros te daremos las pautas para lograrlo.</p>
<p class="text-justify mt-5">Para empezar, debes saber que no hay que tener prisa. Si recomendamos perder peso es para mejorar la salud y sí, seguramente también mejorará tu imagen, pero no tendría sentido verte mejor si pones tu salud al límite.</p>
<p class="text-justify mt-5">Un buen ritmo de pérdida de peso es de alrededor de 0,5kg por semana, lo que serian 2kg al mes y en unos 5 meses habrás logrado perder los 10kg. Así que no te plantees “seguir una dieta”, si no cambiar tus hábitos para siempre, de esta forma no solo conseguirás perder peso si no que además lograrás mantenerlo. Plantearte seguir una dieta para adelgazar 10kg en un mes además de no se realista, ten por seguro que afectará a tu salud y que obtendrás un efecto rebote en los siguientes meses que afectarán a tu capacidad futura de mantener un peso sano</p>
<p class="text-justify mt-5">Cambia de chip ahora mismo, tu objetivo es “a partir de ahora voy a comer de forma saludable”. Esta mentalidad te ayudará a integrar mejor la alimentación a tu ritmo de vida, porque al no tomártelo como algo temporal, habrá que trabajar todas las situaciones, comidas fuera, viajes y otros compromisos sin el peso de “me lo tendré que saltar”.</p>
<p class="text-justify mt-5">No se trata de seguir dietas estrictas en la que se limitan grupos de alimentos. Se trata de comer de todos los grupos de alimentos para obtener todos los nutrientes que nuestro cuerpo necesita. La estrategia se basa en tomar menos calorías de las que gastamos a costa de azúcares y grasas, especialmente las que no son saludables. A esta estrategia se le llama dieta hipocalórica.</p>

<p><u>¿Qué incluye una dieta para adelgazar 10 kg?</u></p>
<p class="text-left  text-darkorange">LÁCTEOS</p>
<p class="text-justify mt-5">Preferiblemente desnatados (leche, yogur y queso fresco 0%). Si tomas bebidas vegetales, elige las enriquecidas en calcio.</p>
<p class="text-left  text-darkorange">VERDURAS</p>
<p class="text-justify mt-5">Todas las verduras son recomendables para bajar de peso rápido. Inclúyelas tanto en la comida como en la cena como plato principal, desde las hojas para ensalada, tomate, pepino… a hortalizas como berenjena o calabacín, o crucíferas (brócoli, coles…) y también las setas.</p>
<p class="text-left  text-darkorange">FRUTAS</p>
<p class="text-justify mt-5">También se aconsejan todas las frutas ¡incluso el plátano! Lo ideal es tomar 2 o 3 raciones cada día. Puedes tomarla de postre o entre horas. Son un tentempié ideal.</p>
<p class="text-left  text-darkorange">CEREALES,LEGUMBRES Y TUBERCULOS</p>
<p class="text-justify mt-5">Este grupo de alimentos es el que utilizamos de forma energética, como si fuera nuestra gasolina. Debemos tomarlo para que el organismo pueda hacer sus funciones, pero debemos controlar la cantidad para que el cuerpo también pueda aprovechar las reservas energéticas (grasa acumulada) y así adelgazar.</p>
<p class="text-justify mt-5">La cantidad orientativa que hay que añadir en la comida y el la cena es la medida de un puño.</p>
<p class="text-justify mt-5">Arroz, pasta, cuscús, pan… preferentemente integral, legumbres (lentejas, garbanzos o alubias) y patata.</p>
<p class="text-left  text-darkorange">PROTÉICOS</p>
<ul class="text-left">
    <li>Huevos: 3-4 huevos a la semana</li>
    <li>Carnes: Preferiblemente blancas (pollo o pavo) y una, máximo dos veces de carne roja (ternera)</li>
    <li>Embutidos recomendados: pechuga de pavo, pollo o jamón cocido.</li>
    <li>Pescados: Aumenta su consumo en detrimento de la carne. Procura tomar mínimo 3 veces por semana pescado blanco (merluza, lenguado, bacalao..) y 1 o 2 veces pescado azul (salmón, boquerones, caballa…)</li>
    <li>Marisco: también puedes incorporarlo en tus recetas como pescado blanco. Almejas, berberechos, calamar, sepia, gambas…</li>
</ul>
<p class="text-left  text-darkorange">ACEITES Y GRASAS</p>
<p class="text-justify mt-5">La más recomendable es el aceite de oliva (mejor en crudo), aunque hay que moderar su cantidad a 2 o 3 cucharadas soperas al día.</p>
<p class="text-justify mt-5">También puedes añadir alguna vez un poquito de aguacate o un puñado de frutos secos tostados o crudos.</p>

