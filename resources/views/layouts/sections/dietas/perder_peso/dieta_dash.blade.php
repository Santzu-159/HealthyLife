
<p class="text-justify mt-5">La dieta DASH responde al acrónimo 'Dietary Approaches to Stop Hypertension' (“enfoque alimenticio para detener la hipertensión”) y, como su nombre indica, fue desarrollada por el Instituto Nacional de Salud estadounidense como una dieta específica para tratar a pacientes con <b>tensión alta</b>. Aunque no nació como una dieta de adelgazamiento, sí que sirve para tal fin.</p>
<p class="text-justify mt-5">Se trata de una dieta muy variada en la que se prima la ingesta de frutas, verduras y lácteos bajos en grasa. También se consumen cereales integrales, carne, pescado y legumbres, pero de forma más restringida. Las <b>grasas saturadas</b>, los azúcares refinados y los <b>carbohidratos</b> están prácticamente prohibidos. La dieta va acompañada de un plan de actividades de ejercicio físico y reduce a la mínima expresión el consumo de alcohol.</p>
<p class="text-justify mt-5">Dado que es una dieta especialmente recomendable para tratar la hipertensión, la sal está limitada al punto justo en que nuestros guisos empiezan a saber a algo.</p>
