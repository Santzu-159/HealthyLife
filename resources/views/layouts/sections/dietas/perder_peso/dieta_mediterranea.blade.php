<p class="text-justify mt-5">La <b>dieta mediterránea</b> es la dieta tradicional que cuenta con un mayor respaldo científico y todos conocemos en qué consiste: un consumo alto de frutas, verduras, pescado, cereales integrales, legumbres y aceite de oliva, y bajo en carne roja, azúcares y grasas saturadas. Pero, además, un componente importante es la práctica de ejercicio. Dado que es la dieta que se utilizaba en nuestro país hasta hace no mucho, es muy fácil de seguir. Pero hay que tener en cuenta que lo que hoy podemos entender como “dieta mediterránea” no siempre corresponde a lo que los científicos entienden como tal.</p>
<p class="text-justify mt-5">Utiliza aceite de oliva. Es el aceite más utilizado en la cocina mediterránea. Es un alimento rico en vitamina E, beta-carotenos y ácidos grasos monoinsaturados que le confieren propiedades cardioprotectoras. Este alimento representa un tesoro dentro de este régimen y ha perdurado a través de siglos entre las costumbres gastronómicas regionales otorgando a los platos un sabor y aroma únicos. Los alimentos poco procesados, frescos y de temporada son los más adecuados. </p>
<p class="text-justify mt-5"> Es importante aprovechar los productos de temporada ya que, sobre todo en el caso de las frutas y verduras, nos permite consumirlas en su mejor momento, tanto a nivel de aportación de nutrientes como por su aroma y sabor.</p>
