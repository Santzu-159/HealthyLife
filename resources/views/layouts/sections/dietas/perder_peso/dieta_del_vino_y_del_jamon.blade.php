<p style="font-size: 30px;"><b>Indicaciones:</b></p>
<p class="text-justify mt-5">Es una dieta hipocalórica, rica en frutas, verduras y proteínas saludables. No pretende sustituir o reemplazar los consejos e indicaciones de los médicos o facultativos. Si estuviera tomando alguna medicación, padeciera alguna enfermedad o estuviera en tratamiento médico, antes de comenzar a aplicar la Dieta del Vino y el Jamón, consúltelo con un médico y siga siempre las indicaciones pre-escritas por el mismo.</p>
<br><p style="font-size: 30px;"><u>Importante:</u></p>
<p class="text-justify mt-5">El patrón de consumo diario recomendado de vino tinto es de una copa de 150ml en el caso de las mujeres y de hasta dos copas de la misma cantidad para los hombres, repartidas en dos ingestas diarias en el caso de éstos últimos.</p>
<p class="text-justify mt-5">Se prohíbe expresamente el consumo de vino tinto, por tratarse de una bebida alcohólica, siempre que se requiera una posterior alerta máxima de los sentidos como en el caso de uso de maquinaria. La conducción o cualquier actividad intelectual o física, recordando que el principal causante de los accidentes de tráfico, tránsito y atropellos se deben, un alto porcentaje de veces, al consumo irresponsable de alcohol.</p>
<p class="text-justify mt-5">Esta dieta está contraindicada en mujeres embarazadas o en periodo de lactancia y menores de 18 años en fase de crecimiento.</p>
