<br><p style="font-size: 40px;"><b>¿QUÉ CANTIDAD DE CADA ALIMENTO SE DEBE CONSUMIR?</b></p><br>
<p class="text-justify mt-5">El concepto de cantidad está unido al de ración. Por ración entendemos la cantidad o porción de alimento adecuada a la medida de un plato «normal»; también puede hacer referencia a una o diversas unidades: huevo, yogur, piezas de fruta, etc. En la tabla de frecuencia recomendada para cada grupo de alimentos, encontrarás las medidas caseras y su peso por ración equivalente para un adulto sano (tabla 5). Pero recuerda que son recomendaciones generales, si presentas poco apetito o algún síntoma específico debes seguir las recomendaciones para esta situación (ver «Si tengo poco apetito y me cuesta comer, ¿Qué puedo hacer») o bien el apartado </p>
