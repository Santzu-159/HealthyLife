<br><p style="font-size: 30px;"><u><b>Alimentos que contienen gluten (prohibidos para celiacos)</b></u></p><br>
<ul class="text-left">
    <li>Pan y harinas de trigo, cebada, centeno y avena.</li>
    <li>Bollos, galletas, bizcochos, magdalenas, pastelería en general.</li>
    <li>Pastas italianas (macarrones, tallarines, pizza…).</li>
    <li>Pastas de sopa.</li>
    <li>Sémola de trigo.</li>
    <li>Productos manufacturados en cuya composición se incluya cualquiera de las harinas citadas y, en general, cualquier tipo de comida, preparado o manufacturado, si el fabricante o comerciante no especifica que “no contiene gluten”.</li>
    <li>Leches malteadas y alimentos malteados.</li>
    <li>Chocolate (excepto si existe declaración expresa del comerciante).</li>
    <li>Bebidas o infusiones preparadas con cereales: malta, cerveza, agua de cebada...</li>
</ul>
<br><p style="font-size: 30px;"><u><b>Alimentos que pueden contener gluten (comprobar)</b></u></p><br>
<ul class="text-left">
    <li>Charcutería en general (mortadela, jamón de york, salchichas, pasteles de jamón o carne…).</li>
    <li>Queso fundido, queso en láminas. También otros quesos sin marcas de garantía.</li>
    <li>Conservas (latas de mejillones, berberechos, fabada en lata, etcétera).</li>
    <li>Patés.</li>
    <li>Dulces y caramelos</li>
    <li>Turrón, mazapán.</li>
    <li>Café y té instantáneos.</li>
</ul>
<br><p style="font-size: 30px;"><u><b>Alimentos que contienen gluten (prohibidos para celiacos)</b></u></p><br>
<ul class="text-left">
    <li>Leche y derivados lácteos (queso, mantequilla, requesón, nata…).</li>
    <li>Carne, pescado y mariscos frescos.</li>
    <li>Huevos</li>
    <li>Frutas.</li>
    <li>Verduras, hortalizas y legumbres.</li>
    <li>Soja</li>
    <li>Arroz, maíz y tapioca (harina y almidón)</li>
    <li>Azúcar</li>
    <li>Miel</li>
    <li>Aceite</li>
    <li>Margarina</li>
    <li>Sal, pimienta y vinagre</li>
    <li>Levaduras sin gluten.</li>
    <li>Sémola de trigo.</li>
    <li>Colorantes.</li>
    <li>Café y té natural</li>
    <li>Manzanilla, Meta Poleo y Hierba Luisa.</li>
    <li>Bebidas Carbónicas.</li>
</ul>
