    {
        $user=Auth::user();

        $diary = new Diary;
        $diary->userDiary = Auth::user()->id;
        $diary->content = $request->content;

        $diary->save();
        
        $mydiary = Diary::orderby("id")->where("userDiary",$user->id)->paginate(3);

        return view('users.mydiary', compact('mydiary','user'));
    }

